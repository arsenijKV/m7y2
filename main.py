@bot.command()
async def choose(ctx, fc: str, sc: str):
     spis.append(fc)
     spis.append(sc)
     random.choice(spis)
     if random.choice(spis) == fc:
          await ctx.send(fc)
     else:
           await ctx.send(sc)    
     spis.remove(fc)
     spis.remove(sc)

